document.addEventListener('DOMContentLoaded', function() {
    const now = new Date();
    const day = now.getDay(); // 0=Կիրակի, 1=Երկուշաբթի,...,6=Շաբաթ
    const hour = now.getHours(); // 0-23

    function randomFrom(arr) {
        return arr[Math.floor(Math.random() * arr.length)];
    }

    let message = "";
    let title = "";

    if (day === 1) { // Երկուշաբթի
        if (hour < 12) {
            message = randomFrom([
                "Երկուշաբթի է, առավոտ, սուրճդ լցրու, դեռ երկար ճանապարհ ունենք։ ☕",
                "Օֆֆֆ, երկուշաբթի առավոտյան վեր կացանք, ուժդ հավաքիր։ 😩",
                "Այս երկուշաբթի առավոտը կարծես կրկնվում է ամեն շաբաթ... 🤦‍♂️"
            ]);
            title = "Երկուշաբթի ☕ — դեռ երկար";
        } else {
            message = randomFrom([
                "Երկուշաբթի երեկո է, քիչ մնաց, համբերի՛ր։ 🌙",
                "Դե լավ, արդեն երկուշաբթի երեկո է, մեկ օրն էլ անցավ։ 👌",
                "Մոտեցանք երկուշաբթի ավարտին, մեկ օրվա տանջանք էլ անցավ։ 💪"
            ]);
            title = "Երկուշաբթի 🌙 — քիչ մնաց";
        }
    } else if (day === 2) { // Երեքշաբթի
        if (hour < 12) {
            message = randomFrom([
                "Երեքշաբթի առավոտ է, դեռ քիչ ճանապարհ ենք անցել։ 😑",
                "Օր 2, դեռ վաղ է ուրախանալու համար։ 🤷‍♂️",
                "Երեքշաբթի առավոտ, դե գնացինք, շարունակում ենք աշխատել։"
            ]);
            title = "Երեքշաբթի 😑 — շարունակենք";
        } else {
            message = randomFrom([
                "Երեքշաբթի երեկո է, քիչ-քիչ մոտենում ենք։ 😉",
                "Դե լավ, երեկոն է արդեն Երեքշաբթիի, քիչ մնաց։",
                "Երեքշաբթին վերջանում է, մնաց 3 օր մինչև ուրբաթ։"
            ]);
            title = "Երեքշաբթի 🌆 — մոտենում ենք";
        }
    } else if (day === 3) { // Չորեքշաբթի
        if (hour < 12) {
            message = randomFrom([
                "Չորեքշաբթի առավոտ է, շաբաթվա մեջտեղում ենք, կապրենք։ 🐪",
                "Կեսը արդեն այստեղ է, Չորեքշաբթի առավոտ։ ✌️",
                "Ողջույն Չորեքշաբթի առավոտ, գնանք դեպի ուրբաթ։"
            ]);
            title = "Չորեքշաբթի 🐪 — կեսը անցավ";
        } else {
            message = randomFrom([
                "Չորեքշաբթի երեկո է, կեսից ավելին անցավ։ 👏",
                "Հանգիստ, Չորեքշաբթին արդեն վերջանում է։ 😌",
                "Այս երեկո արդեն Չորեքշաբթի է, մոտենում ենք նպատակին։"
            ]);
            title = "Չորեքշաբթի 🌜 — քիչ մնաց";
        }
    } else if (day === 4) { // Հինգշաբթի
        if (hour < 12) {
            message = randomFrom([
                "Հինգշաբթի առավոտ է, գրեթե հասանք ուրբաթին։ 🤗",
                "Ահա արդեն Հինգշաբթի, վաղը ուրբաթ է։",
                "Հինգշաբթի առավոտ, պահիր ուժերդ վերջի համար։"
            ]);
            title = "Հինգշաբթի 🤗 — գրեթե այնտեղ";
        } else {
            message = randomFrom([
                "Հինգշաբթի երեկո է, վաղը արդեն ուրբաթ։ 🎉",
                "Այսօր արդեն Հինգշաբթի երեկո է, պատրաստվիր ուրախությանը։",
                "Դե քիչ մնաց, Հինգշաբթի երեկո է։"
            ]);
            title = "Հինգշաբթի 🌃 — քիչ մնաց ուրբաթին";
        }
    } else if (day === 5) { // Ուրբաթ
        if (hour < 12) {
            message = randomFrom([
                "Վերջապես ուրբաթ առավոտ է, դիմացիր քիչ էլ։ 🥳",
                "Այսօր ուրբաթ է, առավոտից իսկ արդեն տրամադրությունը բարձր։ 🎈",
                "Ուրբաթ առավոտ է, երեկոյան խնջույք, այո՞։ 🍻"
            ]);
            title = "Ուրբաթ 🥳 — վերջապես";
        } else {
            message = randomFrom([
                "Ուրբաթ երեկո է, սա վերջապես տոն է։ 🎉",
                "Ուրբաթ երեկո, այլևս աշխատանքի մասին մի մտածիր։ 🕺",
                "Ուրբաթ երեկո, գնա հանգստանալու։ 😎"
            ]);
            title = "Ուրբաթ 🎉 — վայելիր";
        }
        document.body.style.background = "#d1ffd1";
    } else if (day === 6) { // Շաբաթ
        message = randomFrom([
            "Շաբաթ է, քնիր որքան ուզում ես։ 🛌",
            "Այսօր Շաբաթ է, վայելիր հանգստիդ օրը։ ☀️",
            "Շաբաթ, լավ ժամանակ է ընկերների հետ անցկացնել։ 🍕"
        ]);
        title = "Շաբաթ 😎 — վայելիր";
    } else if (day === 0) { // Կիրակի
        if (hour < 18) {
            message = randomFrom([
                "Կիրակի է, դեռ շատ ժամանակ կա հանգստանալու համար։ 😌",
                "Այսօր Կիրակի, բաց թող բոլոր հոգսերդ։",
                "Վայելիր այս Կիրակին, վաղը նորից կսկսենք։"
            ]);
            title = "Կիրակի 😌 — հանգստի օր";
        } else {
            message = randomFrom([
                "Կիրակի երեկո է... հապա պատրաստվիր վաղվա երկուշաբթիին։ 😬",
                "Կիրակի երեկո, վաղը ցավոք աշխատանք է։",
                "Էհ, Կիրակի երեկո, դե լավ, կդիմանանք։"
            ]);
            title = "Կիրակի 😬 — վաղը երկուշաբթի";
        }
    }

    const messageEl = document.getElementById('message');
    messageEl.textContent = message;
    document.title = title;
});
